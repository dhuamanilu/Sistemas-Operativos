#include <stdio.h>

int main() {
    // Mostrar el dato 'A' de tipo char
    char datoChar = 'A';
    printf("Dato char: %c\n", datoChar);

    // Mostrar el número 3 como tipo de dato int
    int datoInt = 3;
    printf("Dato int: %d\n", datoInt);

    // Mostrar el número 3 como tipo de dato float
    float datoFloat = 3.0;
    printf("Dato float: %.2f\n", datoFloat);

    // Mostrar el número 3 como tipo de dato double
    double datoDouble = 3.0;
    printf("Dato double: %.2lf\n", datoDouble);

    return 0;
}
